def _bundled_base() -> dict:
    """번들된 prompts.json의 '기본'(배포 시 고정된 캐논). 사용자가 못 지우게 항상 이걸로 고정.
    번들에서 못 읽으면 DEFAULT_PROMPTS['기본']로 폴백."""
    try:
        from app_paths import get_bundle_dir
        p = os.path.join(get_bundle_dir(), "prompts.json")
        with open(p, "r", encoding="utf-8") as f:
            b = json.load(f).get("기본") or {}
        if (b.get("blog") or "").strip():
            return b
    except Exception:
        pass
    return DEFAULT_PROMPTS["기본"]


def get_base_prompt() -> dict:
    """수정·삭제 불가인 '기본' 프롬프트(캐논) 사본."""
    return dict(_bundled_base())


def load_prompts() -> dict:
    merged = dict(DEFAULT_PROMPTS)
    path = _prompts_file()
    if os.path.exists(path):
        try:
            with open(path, "r", encoding="utf-8") as f:
                saved = json.load(f)
            merged = {**DEFAULT_PROMPTS, **saved}
        except Exception:
            merged = dict(DEFAULT_PROMPTS)
    # '기본'은 항상 번들 캐논으로 고정 — 사용자가 비우거나 지워도 자동 복구
    merged["기본"] = dict(_bundled_base())
    return merged


def save_prompts(prompts: dict):
    with open(_prompts_file(), "w", encoding="utf-8") as f:
        json.dump(prompts, f, ensure_ascii=False, indent=2)


def get_prompt_for_keyword(keyword: str, prompts: dict = None) -> dict:
    """검색 키워드에서 업종을 추출하고 매칭되는 프롬프트 반환"""
    if prompts is None:
        prompts = load_prompts()

    # 키워드의 마지막 단어가 업종
    parts = keyword.strip().split()
    biz_type = parts[-1] if parts else ""

    # 업종명으로 프롬프트 검색
    for key in prompts:
        if key in biz_type or biz_type in key:
            return prompts[key]

    # 카테고리 키워드로 검색
    for key in prompts:
        if key in keyword:
            return prompts[key]

    # 없으면 기본 프롬프트
    return prompts.get("기본", DEFAULT_PROMPTS["기본"])
